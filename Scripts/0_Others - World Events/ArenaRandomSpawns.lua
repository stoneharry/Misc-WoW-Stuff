function on_theme_delete()
	if (r_time) and (os.time() >= r_time) then
		for k, object in pairs(go_table) do
			if (object:IsInWorld()) then
				object:Despawn(0,0)
				go_table[k] = nil;
			end	
		end
		r_time = nil;
	end
end	

CreateLuaEvent(on_theme_delete, 5000, 0)

function On_Theme_TriggerNPC(pUnit, Event)
	pUnit:RegisterEvent("Declare_Self_trigger", 5000, 0)
end

function Declare_Self_trigger(pUnit)
	if not (r_time) then
		i = i + 1
		if (i == 6) then
			i = 1
		end
		local tbl = theme_table[i]
		for info in pairs(tbl) do
			local object = pUnit:SpawnGameObject(unpack(tbl[info]))
			table.insert(go_table, object)
		end
		r_time = os.time() + math.random(30,600);
	end
end

RegisterUnitEvent(100002, 18, "On_Theme_TriggerNPC")